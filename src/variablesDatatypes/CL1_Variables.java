package variablesDatatypes;

public class CL1_Variables {

	public static void main(String args[])
	{
		//Approach 1
		int a;	//variable Declaration 
		a=100;	//variable initialization 
		System.out.println("a: "+a);	//Variable usage
		
		//Approach 2
		int b=100;	//Variable Declaration + Initialization
		System.out.println("b: "+b); //Variable Usage
		
		
		//Approach 3
		int c,d,e; //Multiple variable declaration at same time of same type
		c=100;d=200;e=300;	//multiple variable initialization
		System.out.println("c: "+c+"\nd: "+d+"\ne: "+e);	//Variable usage
		
		//Approach 4
		int x=100, y=200, z=300;	//Multiple variable declaration & initialization at same time
		System.out.println("Add(x+y+z): "+(x+y+z));	//Variable usage
		
		
		
		
		
	}
}
